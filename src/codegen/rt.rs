/// Runtime profile requested by a caller.
///
/// Host oracles need the core runtime and libm bridge, but not native
/// continuation metadata. Native backend links include the full runtime surface.
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum RuntimeProfile {
    HostOracle,
    NativeBackend,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
enum RuntimeModule {
    Memory,
    String,
    Integer,
    Float,
    LibmBridge,
    Effect,
    Sort,
    Array,
    Buffer,
    NativeKont,
    Io,
}

impl RuntimeProfile {
    const fn modules(self) -> &'static [RuntimeModule] {
        match self {
            Self::HostOracle => BASE_RUNTIME_MODULES,
            Self::NativeBackend => NATIVE_RUNTIME_MODULES,
        }
    }
}

impl RuntimeModule {
    const fn files(self) -> &'static [&'static str] {
        match self {
            Self::Memory => &[PRISM_INTERNAL_H, PRISM_MEM_H, PRISM_MEM_C],
            Self::String => &[PRISM_STRING_H, PRISM_STRING_C],
            Self::Integer => &[PRISM_INT_H, PRISM_INT_C],
            Self::Float => &[PRISM_FLOAT_H, PRISM_FLOAT_C],
            Self::LibmBridge => &[PRISM_LIBM_H, PRISM_LIBM_RENAME_H, PRISM_LIBM_C],
            Self::Effect => &[PRISM_EFFECT_H, PRISM_EFFECT_C],
            Self::Sort => &[PRISM_SORT_H, PRISM_SORT_C],
            Self::Array => &[PRISM_ARRAY_H, PRISM_ARRAY_C],
            Self::Buffer => &[PRISM_BUFFER_H, PRISM_BUFFER_C],
            Self::NativeKont => &[PRISM_KONT_H, PRISM_KONT_C],
            Self::Io => &[PRISM_IO_H, PRISM_IO_C],
        }
    }
}

fn runtime_entry(name: &str) -> io::Result<(&'static str, bool)> {
    RUNTIME_FILES
        .iter()
        .find_map(|(entry_name, body, is_header)| {
            (*entry_name == name).then_some((*body, *is_header))
        })
        .ok_or_else(|| {
            io::Error::new(
                ErrorKind::InvalidData,
                format!("runtime manifest is missing `{name}`"),
            )
        })
}

/// Write the native-backend runtime profile into `dir` for the C compiler.
///
/// Returns the absolute paths of the `.c` translation units to hand to the
/// compile step. The headers land beside the sources so each
/// `#include "prism_*.h"` resolves.
///
/// # Errors
/// Fails on any filesystem error creating `dir` or writing a runtime file.
#[cfg(feature = "native")]
pub fn write_runtime(dir: &Path) -> io::Result<Vec<PathBuf>> {
    write_runtime_for(dir, RuntimeProfile::NativeBackend)
}

/// Write the selected runtime profile into `dir` for the C compiler.
///
/// # Errors
/// Fails on any filesystem error, or if the generated runtime manifest omits a
/// file named by the selected profile.
#[cfg(feature = "native")]
pub fn write_runtime_for(dir: &Path, profile: RuntimeProfile) -> io::Result<Vec<PathBuf>> {
    fs::create_dir_all(dir)?;
    let mut sources = Vec::new();
    for module in profile.modules() {
        for name in module.files() {
            let (body, is_header) = runtime_entry(name)?;
            let path = dir.join(name);
            if let Some(parent) = path.parent() {
                fs::create_dir_all(parent)?;
            }
            fs::write(&path, body)?;
            if !is_header {
                sources.push(path);
            }
        }
    }
    Ok(sources)
}

/// The single vendored-libm archive, compiled exactly once by build.rs.
///
/// Embedded here so both the interpreter (which links it at build time) and every
/// native binary (via [`write_libm_archive`] + the link step) use THESE bytes, and
/// the non-correctly-rounded transcendentals stay bit-identical across backends. A
/// second, differently-invoked recompile is what let them drift by a ULP.
#[cfg(feature = "native")]
pub const LIBM_ARCHIVE: &[u8] = include_bytes!(env!("PRISM_LIBM_ARCHIVE"));

/// Materialize the embedded libm archive into `dir` and return its path, for the
/// native link step to link directly instead of recompiling the libm sources.
///
/// # Errors
/// Fails on any filesystem error writing the archive.
#[cfg(feature = "native")]
pub fn write_libm_archive(dir: &Path) -> io::Result<PathBuf> {
    let path = dir.join("libprism_libm.a");
    fs::write(&path, LIBM_ARCHIVE)?;
    Ok(path)
}

