//! Single source of truth for the C runtime symbols codegen calls by name.
//!
//! These are the `prism_*` intrinsics defined in the `runtime/` C modules that the
//! emitter references directly: allocation, reference counting, boxing, the IO
//! ops, local mutable refs, the bignum arithmetic helpers, and the effect-machine
//! instrumentation. Spelling each as a named constant here (rather than a string
//! literal scattered across the emitter) means a rename happens in one place, and
//! the drift guard below pins every name to a definition in the runtime so a
//! mismatch fails the build instead of the linker.
//!
//! The surface builtins (`concat`, `show_int`, the fixed-width arithmetic, ...)
//! are not here: their symbols are derived once from [`crate::core::builtins::Builtin::sym`].
//!
//! This module also owns the embedded copy of the runtime. `build.rs` holds the
//! one canonical list of runtime files and generates a manifest of `(name, text,
//! is_header)` tuples ([`RUNTIME_FILES`]); the native link path writes those files
//! and compiles the sources ([`write_runtime`]), so the in-binary runtime and the
//! build-script-compiled runtime derive from the same list and cannot drift.

use std::fs;
use std::io;
use std::path::{Path, PathBuf};

// The runtime file table, generated by build.rs from its canonical source list.
include!(concat!(env!("OUT_DIR"), "/runtime_manifest.rs"));

/// Write every runtime file into `dir` for the C compiler.
///
/// Returns the absolute paths of the `.c` translation units to hand to the
/// compile step. The headers land beside the sources so each
/// `#include "prism_*.h"` resolves.
///
/// # Errors
/// Fails on any filesystem error creating `dir` or writing a runtime file.
#[cfg(feature = "native")]
pub fn write_runtime(dir: &Path) -> io::Result<Vec<PathBuf>> {
    fs::create_dir_all(dir)?;
    let mut sources = Vec::new();
    for (name, body, is_header) in RUNTIME_FILES {
        let path = dir.join(name);
        // Vendored libm files carry a `libm/` prefix; create the subdirectory so
        // the write lands and each `#include "libm.h"` resolves beside them.
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)?;
        }
        fs::write(&path, body)?;
        if !is_header {
            sources.push(path);
        }
    }
    Ok(sources)
}

