// Canonicalize a raw i64 into an `Int`: a tagged immediate when it fits 63 bits,
// a bignum cell otherwise. The float->int conversions saturate to the full i64
// range (matching the interpreter's `f as i64`), and i64::MAX/MIN overflow the
// 63-bit tag, so the result must promote here rather than be inline-retagged.
pub(super) const INT_OF_LONG: &str = "prism_int_of_long";

// Effect-machine instrumentation and the two miscompile traps: the
// over-application trap and the non-exhaustive-`case` trap.
pub(super) const EFFOP_ALLOC: &str = "prism_effop_alloc";
pub(super) const DRIVE_STEP: &str = "prism_drive_step";
pub(super) const APPLY_ERROR: &str = "prism_apply_error";
pub(super) const MATCH_ERROR: &str = "prism_match_error";

