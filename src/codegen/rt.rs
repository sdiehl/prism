#[cfg(test)]
mod tests {
    #[cfg(feature = "native")]
    use std::process::Command;
    #[cfg(feature = "native")]
    use std::sync::atomic::{AtomicU64, Ordering};

    use super::{native_kont, ALL};

    const KONT_RUNTIME_FILES: &[&str] = &["prism_kont.h", "prism_kont.c"];
    const KONT_TEST_SCHEME: &str = "prism-core-hash-v1";
    const KONT_TEST_BUNDLE: &str = "0123456789abcdef";
    const KONT_TEST_HASH: &str = "9d18022b96cb1349a4de4ab500879606121e86d736be1fc2da3f69385c9fcd35";
    const KONT_TEST_SYMBOL: &str = "prism_main";
    const KONT_TEST_CORE_NAME: &str = "main";
    const KONT_LOOKUP_HARNESS: &str = include_str!("../../tests/fixtures/native_kont_lookup.c");
    const KONT_HARNESS_REPLACEMENTS: [(&str, &str); 5] = [
        ("@KONT_TEST_SCHEME@", KONT_TEST_SCHEME),
        ("@KONT_TEST_BUNDLE@", KONT_TEST_BUNDLE),
        ("@KONT_TEST_HASH@", KONT_TEST_HASH),
        ("@KONT_TEST_SYMBOL@", KONT_TEST_SYMBOL),
        ("@KONT_TEST_CORE_NAME@", KONT_TEST_CORE_NAME),
    ];
    #[cfg(feature = "native")]
    static KONT_TEST_SEQ: AtomicU64 = AtomicU64::new(0);

    fn kont_lookup_harness() -> String {
        let mut body = KONT_LOOKUP_HARNESS.to_string();
        for (needle, replacement) in KONT_HARNESS_REPLACEMENTS {
            body = body.replace(needle, replacement);
        }
        body
    }

    // Every runtime symbol the emitter calls must be defined in the C runtime.
    // A rename on one side without the other would otherwise surface only as a
    // link failure (or, worse, a call to a stale symbol); this turns it into a
    // build failure naming the offender.
    #[test]
    fn symbols_defined_in_runtime() {
        for sym in ALL {
            let defined = super::RUNTIME_FILES
                .iter()
                .any(|(_, body, _)| body.contains(sym));
            assert!(
                defined,
                "runtime symbol `{sym}` (codegen::rt) is not defined in any runtime/ module"
            );
        }
    }

    #[test]
    fn native_kont_runtime_surface_is_manifested() {
        for file in KONT_RUNTIME_FILES {
            assert!(
                super::RUNTIME_FILES.iter().any(|(name, _, _)| name == file),
                "runtime kont file `{file}` is missing from the embedded runtime manifest"
            );
        }
        for sym in native_kont::RUNTIME_SURFACE_SYMBOLS {
            let defined = super::RUNTIME_FILES
                .iter()
                .any(|(_, body, _)| body.contains(sym));
            assert!(
                defined,
                "native kont runtime symbol `{sym}` is not defined in any runtime/ module"
            );
        }
    }

    #[cfg(all(feature = "native", not(target_arch = "wasm32")))]
    fn run_kont_lookup_harness(extra_cflags: &[&str]) {
        let cc = std::env::var("PRISM_CC").unwrap_or_else(|_| env!("PRISM_BUILD_CC").into());
        if !Command::new(&cc)
            .arg("--version")
            .output()
            .is_ok_and(|o| o.status.success())
        {
            eprintln!("skipping native kont runtime lookup: C compiler `{cc}` not found");
            return;
        }

        let stem = format!(
            "prism_kont_lookup_{}_{}",
            std::process::id(),
            KONT_TEST_SEQ.fetch_add(1, Ordering::Relaxed)
        );
        let dir = std::env::temp_dir().join(&stem);
        std::fs::create_dir_all(&dir).unwrap();
        let rt_sources = super::write_runtime(&dir).unwrap();
        let libm_archive = super::write_libm_archive(&dir).unwrap();
        let src = dir.join(format!("{stem}.c"));
        let bin = dir.join(&stem);
        std::fs::write(&src, kont_lookup_harness()).unwrap();
        let mut cmd = Command::new(&cc);
        cmd.args(["-O0", "-w", "-DPRISM_NATIVE_KONT_FRAMES"]);
        if cfg!(target_os = "linux") {
            cmd.arg("-rdynamic");
        }
        let comp = cmd
            .args(extra_cflags)
            .arg(&src)
            .args(&rt_sources)
            .arg(&libm_archive)
            .arg("-o")
            .arg(&bin)
            .output()
            .unwrap();
        assert!(
            comp.status.success(),
            "native kont runtime lookup harness failed to compile:\n{}",
            String::from_utf8_lossy(&comp.stderr)
        );
        let run = Command::new(&bin).output().unwrap();
        let _ = std::fs::remove_dir_all(&dir);
        assert!(
            run.status.success(),
            "native kont runtime lookup harness exited with {:?}\nstdout:\n{}\nstderr:\n{}",
            run.status,
            String::from_utf8_lossy(&run.stdout),
            String::from_utf8_lossy(&run.stderr)
        );
    }

    #[cfg(all(feature = "native", not(target_arch = "wasm32")))]
    #[test]
    fn native_kont_runtime_lookup_reads_embedded_table() {
        run_kont_lookup_harness(&[]);
    }

    #[cfg(all(feature = "native", not(target_arch = "wasm32")))]
    #[test]
    fn native_kont_runtime_refuses_missing_pointer_table() {
        run_kont_lookup_harness(&["-DPRISM_KONT_NO_PTR_TABLE"]);
    }
}
